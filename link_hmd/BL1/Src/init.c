void sysInit(void)
{
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

    /* Configure the system clock */
    SystemClock_Config();
    flash_init();
    set_pcbid(XB02);
    get_pcbid(&pcb_id);
    if(pcb_id == 0xFFFFFFFF){
      pcb_id = XA0n;
      set_pcbid(pcb_id);
    }

    /* Initialize all configured peripherals */
    gpio_init(pcb_id);
    RTOS_I2C_Init();
	ioexp_drv_init();
    dbg_uart_init(pcb_id);
    //MX_USB_OTG_FS_PCD_Init();
	bsp_pmic_init();
	display_drv_init();
}


